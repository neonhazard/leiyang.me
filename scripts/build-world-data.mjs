/* eslint-env node */
// One-time generator for the world-countries data files.
// Reads world-atlas (countries-50m topojson) + world-countries (metadata),
// then emits one self-contained TS file per projection under src/constants/.
//
// Run: node scripts/build-world-data.mjs

import fs from "node:fs";
import path from "node:path";
import { feature } from "topojson-client";
import { geoPath, geoMercator, geoEquirectangular, geoArea } from "d3-geo";
import { geoMiller } from "d3-geo-projection";

const ROOT = path.resolve(import.meta.dirname, "..");
const OUT_DIR = path.join(ROOT, "src/constants");

const EARTH_AREA_KM2 = 510_072_000;
const PATH_PRECISION = 0;
// Rotate every projection by −25° east so the antimeridian cut falls in the
// open mid-Pacific instead of slicing through the Aleutian Islands (which
// would otherwise wrap to the far edge of the map and inflate the world's
// bounding box).
const ROTATION = [-25, 0];

// Projection configs. width/height is the SVG viewBox; we use natural aspect
// ratios so each projection fills its container without letterboxing.
const PROJECTIONS = [
  {
    key: "mercator",
    label: "Web Mercator",
    width: 1000,
    height: 1000,
    factory: () => geoMercator(),
  },
  {
    key: "miller",
    label: "Miller",
    width: 1000,
    height: 735,
    factory: () => geoMiller(),
  },
  {
    key: "equirectangular",
    label: "Equirectangular",
    width: 1000,
    height: 500,
    factory: () => geoEquirectangular(),
  },
];

const topo = JSON.parse(
  fs.readFileSync(path.join(ROOT, "node_modules/world-atlas/countries-50m.json"), "utf8"),
);
const meta = JSON.parse(
  fs.readFileSync(path.join(ROOT, "node_modules/world-countries/countries.json"), "utf8"),
);

const collection = feature(topo, topo.objects.countries);

const byCcn3 = new Map();
for (const c of meta) {
  if (!c.ccn3) continue;
  byCcn3.set(c.ccn3.padStart(3, "0"), c);
}

// Vatican is a UN permanent observer, not a member. world-countries flags it
// as unMember=true which is wrong. Override to keep the 193 UN-member total
// correct.
const UN_OVERRIDES = { VA: false };

function continentOf(c) {
  if (c.region === "Antarctic") return "Antarctica";
  if (c.region === "Americas") {
    return c.subregion === "South America" ? "South America" : "North America";
  }
  return c.region;
}

function buildRecords(config) {
  const projection = config.factory().rotate(ROTATION).fitSize([config.width, config.height], collection);
  const toPath = geoPath(projection).digits(PATH_PRECISION);

  const byIso = new Map();
  for (const feat of collection.features) {
    const ccn3 = String(feat.id).padStart(3, "0");
    const m = byCcn3.get(ccn3);
    if (!m) continue;
    const iso = m.cca2;
    const d = toPath(feat);
    if (!d) continue;

    const existing = byIso.get(iso);
    if (existing) {
      existing.d = existing.d + " " + d;
      continue;
    }

    let areaKm2 = m.area;
    if (!areaKm2 || areaKm2 <= 0) {
      areaKm2 = Math.round(geoArea(feat) * (EARTH_AREA_KM2 / (4 * Math.PI)));
    }

    byIso.set(iso, {
      iso,
      name: m.name.common,
      continent: continentOf(m),
      isUNMember: UN_OVERRIDES[iso] ?? m.unMember,
      areaKm2,
      d,
    });
  }

  const records = [...byIso.values()].sort((a, b) => a.name.localeCompare(b.name));

  const presentIsos = new Set(records.map((r) => r.iso));
  const missingShape = meta
    .filter((c) => c.cca2 && !presentIsos.has(c.cca2))
    .map((c) => ({
      iso: c.cca2,
      name: c.name.common,
      continent: continentOf(c),
      isUNMember: UN_OVERRIDES[c.cca2] ?? c.unMember,
      areaKm2: c.area || 0,
      d: "",
    }))
    .sort((a, b) => a.name.localeCompare(b.name));

  return [...records, ...missingShape];
}

const typesHeader = `// Auto-generated by scripts/build-world-data.mjs — do not edit by hand.

export type Continent =
  | "Africa"
  | "Antarctica"
  | "Asia"
  | "Europe"
  | "North America"
  | "Oceania"
  | "South America";

export interface Country {
  /** ISO 3166-1 alpha-2 */
  iso: string;
  name: string;
  continent: Continent;
  isUNMember: boolean;
  areaKm2: number;
  /** SVG path d-attribute in the projection's viewBox. Empty if not drawn at 1:50m. */
  d: string;
}

export interface WorldData {
  COUNTRIES: readonly Country[];
  MAP_VIEW_WIDTH: number;
  MAP_VIEW_HEIGHT: number;
}
`;

// Shared types file
fs.writeFileSync(path.join(OUT_DIR, "world-countries.ts"), typesHeader, "utf8");
console.log("Wrote src/constants/world-countries.ts (types)");

for (const config of PROJECTIONS) {
  const data = buildRecords(config);

  const json = JSON.stringify(data);
  const escaped = json.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
  const body = `// Auto-generated by scripts/build-world-data.mjs — do not edit by hand.
// Source: world-atlas (countries-50m, public domain) + world-countries (ODbL).
// Projection: ${config.label} (rotated ${ROTATION[0]}°), fit to ${config.width}x${config.height}.

import type { Country } from "./world-countries";

export const MAP_VIEW_WIDTH = ${config.width};
export const MAP_VIEW_HEIGHT = ${config.height};

export const COUNTRIES: readonly Country[] = JSON.parse(\`${escaped}\`);
`;

  const file = path.join(OUT_DIR, `world-countries-${config.key}.ts`);
  fs.writeFileSync(file, body, "utf8");

  const kb = Math.round(fs.statSync(file).size / 1024);
  const unCount = data.filter((c) => c.isUNMember).length;
  const withShape = data.filter((c) => c.d).length;
  console.log(
    `Wrote src/constants/world-countries-${config.key}.ts  (${data.length} entries, ${withShape} with shape, ${unCount} UN, ~${kb} KB)`,
  );
}
